#include <iostream>
#include <vector>
#include <string>
#include <stack>
using namespace std;
int main()
{
    int N;
    cin >> N;
    vector<string> arr;
    for (int i = 0; i < N; i++)
    {
        string input;
        cin >> input;
        arr.push_back(input);
    }
    for (int i = 0; i < N; i++)
    {
        stack<char> s;
        int j;
        for (j = 0; j < arr[i].length(); j++)
        {
            if (arr[i].at(j) == '(')
            {
                s.push('(');
            }
            else
            {
                if (s.empty())
                {
                    break;
                }
                s.pop();
            }
        }
        if (!s.empty() || j != arr[i].length())
        {
            cout << "NO" << '\n';
        }
        else
        {
            cout << "YES" << '\n';
        }
    }
    return 0;
}

