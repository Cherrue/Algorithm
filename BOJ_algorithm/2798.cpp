#include <iostream>
#include <vector>
using namespace std;
int main()
{
    int N, M;
    scanf("%d%d", &N, &M);
    int arr[N];
    for (int i = 0; i < N; i++)
    {
        scanf("%d", &arr[i]);
    }
    int near = 0;
    for (int i = 0; i < N; i++)
    {
        for (int j = 0; j < N; j++)
        {
            if (j == i)
                continue;
            for (int k = 0; k < N; k++)
            {
                if (k == i || k == j)
                    continue;
                int temp = arr[i] + arr[j] + arr[k];
                if (temp < M && temp > near)
                {
                    near = temp;
                }
                else if (temp == M)
                {
                    cout << temp;
                    return 0;
                }
            }
        }
    }
    cout << near;
    return 0;
}

