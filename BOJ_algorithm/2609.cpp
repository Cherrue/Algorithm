#include <iostream>
#include <vector>
using namespace std;
int main()
{
    int num1, num2;
    cin >> num1 >> num2;
    int a = num1, b = num2;
    int c;
    while (b != 0)
    {
        c = a % b;
        a = b;
        b = c;
    }
    cout << a << "\n";
    cout << a * (num1 / a) * (num2 / a);
    return 0;
}

