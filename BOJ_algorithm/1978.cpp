#include <iostream>
#include <vector>
using namespace std;

int main()
{
    int N, cnt = 0;
    int j = 0;
    cin >> N;
    for (int i = 0; i < N; i++)
    {
        int temp;
        cin >> temp;
        for (j = 2; j < temp; j++)
        {
            if (temp % j == 0)
            {
                break;
            }
        }
        if (j == temp)
        {
            cnt++;
        }
    }
    cout << cnt;
    return 0;
}

